import turtle as t
print(t.shape('turtle'))

# 터틀 그래픽스 (기본 거북이 출력)


print(t.forward(100))

# 거북이를 100 픽셀만큼 앞으로 이동

print(t.right(90))

# 방향 지정(right를 사용해 오른쪽 90도가 되도록 지정)


print(t.forward(100))

# 거북이를 100픽셀만큼 앞으로 이동



print(t.right(90),t.forward(100),t.right(90),t.forward(100))

# 사각형 그리기
# ( 앞으로 : forward,fd  뒤로 : backward,bk,back   왼쪽으로 : left,lt  오른쪽 : right,rt )

