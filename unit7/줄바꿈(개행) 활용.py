print('\tHi')
print('\nHi')
print('\\Hi')

# \n : 개행 /  \t : tab  / \\ : \ 문자 자체를 출력

print(' ')
print(1)
print(2)

# print 자체적으로 뒤에 \n이 붙어서 다음 줄로 출력됨

print(' ')
print(1, end= ' ')
print(2)

# end 에 빈 문자열을 지정하면 숫자가 세로가 아닌 한 줄로 붙어서 출력 됨
